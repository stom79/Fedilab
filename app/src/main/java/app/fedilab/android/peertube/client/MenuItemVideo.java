public class MenuItemVideo {

    int icon;
    String title;
    actionType action;

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public actionType getAction() {
        return action;
    }

    public void setAction(actionType action) {
        this.action = action;
    }

    public enum actionType {
        RESOLUTION,
        SPEED,
        CAPTION,
        AUTONEXT
    }
}
